package com.orientacaoobjeto;

public class SomaAB {

    private double somaAB;
    private double a;
    private double b;
    private double c;

    public SomaAB(double somaAB, double a, double b, double c) {
        this.somaAB = somaAB;
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getSomaAB() {
        somaAB = a + b;
        if (somaAB > c);
        return c;
    }
}